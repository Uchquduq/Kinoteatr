<template>
  <div class="movies-pagination d-flex justify-content-center">
    <BPagination 
    v-model="currentPageModel"
    :per-page="perPage"
    :total-rows="total"
    prev-text="Prev"
    next-text="Next"
    />
    
    <b-form-input type="number" class="ml-2 mr-sm-2 pl-2 pr-2" style="background: inherit ; color: white;max-width: 70px; height:32px" v-model.number="currentPage"></b-form-input>
    
  </div>
</template>

<script>
export default {
  name: "MoviesPagination",
  props: {
    currentPage: {
      type: Number,
      default: 1
    },
    total: {
      type: Number,
      default: 1
    },
    perPage: {
      type: Number,
      default: 1
    },
  },
  computed: {
    currentPageModel: {
      get() {
        return this.currentPage;
      },
      set(value) {
        this.$emit("pageChanged", value)
      }
    }
  }
};
</script>

